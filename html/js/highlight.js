function mouseoverBox1(){
    var myPara = document.getElementById("spider");
    myPara.style.transform = "scale(1.1)";
    myPara.style.filter = "brightness(120%)";
}
function onmouseoutBox1(){
    var myPara = document.getElementById("spider");
    myPara.style.transform = "scale(1)";
    myPara.style.filter = "brightness(40%)";
}
